    <h1>Regulamento da Biblioteca</h1>
    <h2>Introdução</h2>
    <p>Este regulamento tem como objetivo estabelecer as normas de uso e comportamento dos usuários da biblioteca, garantindo um ambiente adequado para todos.</p>

    <div class="section-title">1. Regras de Empréstimo</div>
    <ul class="list">
        <li>Os materiais podem ser emprestados por até 14 dias.</li>
        <li>A renovação do empréstimo pode ser realizada por mais 7 dias, desde que não haja reservas para o item.</li>
        <li>É permitido emprestar no máximo 3 livros por vez.</li>
    </ul>

    <div class="section-title">2. Regras de Comportamento</div>
    <ul class="list">
        <li>É proibido falar alto ou fazer barulho nas áreas de leitura.</li>
        <li>Não é permitido consumir alimentos ou bebidas nas áreas de leitura.</li>
        <li>O uso de celulares deve ser restrito a chamadas urgentes ou mensagens.</li>
    </ul>

    <div class="section-title">3. Acesso ao Acervo</div>
    <p>O acervo da biblioteca é dividido em materiais de consulta livre e materiais de consulta restrita. Para acessar os materiais restritos, é necessário pedir autorização ao bibliotecário responsável.</p>

    <div class="section-title">4. Penalidades</div>
    <ul class="list">
        <li>Multas diárias de R$1,00 por atraso na devolução de materiais.</li>
        <li>Suspensão temporária de acesso ao acervo em caso de comportamento inadequado.</li>
    </ul>

    <p>Este regulamento entra em vigor a partir da data de sua publicação e pode ser alterado a qualquer momento, conforme as necessidades da biblioteca.</p>
